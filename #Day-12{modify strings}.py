# Python - Modify Strings

'''Upper Case'''
p = " python tutorial"
print(p.upper())

'''Lower Case'''
print(p.lower())

'''Remove Whitespace'''
print(p.strip()) # "strip()" method removes any whitespace from the beginning or the end

'''Replace String'''
print(p.replace('py', 'jy'))

'''Split String'''
print(p.split()) # break and formed a list items